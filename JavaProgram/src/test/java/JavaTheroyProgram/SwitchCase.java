package JavaTheroyProgram;

public class SwitchCase {

	
	public static void main(String[] args) {
		
	   int 	x=6;
		switch(x)
		{
		case 1 :
		System.out.print("One");
		break;
		
		case 2 :
			System.out.print("two");
			break;
			
			
		case 5 :
			System.out.print("five");
			break;
			
		default:
			System.out.print("not match");
		}
		
		
		
			
	}
}
